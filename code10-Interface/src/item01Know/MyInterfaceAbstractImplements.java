package item01Know;

public class MyInterfaceAbstractImplements implements MyInterfaceAbstract {
    @Override
    public void methodA() {
        System.out.println("第一个方法");
    }

    @Override
    public void methodB() {
        System.out.println("第二个方法");
    }

    @Override
    public void methodC() {
        System.out.println("第三个方法");
    }

    @Override
    public void methodD() {
        System.out.println("第四个方法");
    }
}
